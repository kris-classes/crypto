# Using the print() function. Lines that begin with # are "comments" and are ignored by Python. They're used to tell other programmers (or yourself) what you want the code to do -- not necessarily what the code actually does.


print('hello')

print(123)

print(42.0324)

print(1 + 2)  # Comments can also go at the end of a line.

print('hello' + 'world')  # Python will join these together, but we'll use a better way to do this later on.
